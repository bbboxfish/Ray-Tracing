#[derive(Clone, Debug, PartialEq,Copy)]
#[derive(Default)]
pub struct Vec3 {
    pub x: f64,
    pub y: f64,
    pub z: f64,
}
impl Vec3 {
    pub fn x(&self) -> f64 {
        self.x
    }
    pub fn y(&self) -> f64 {
        self.y
    }
    pub fn z(&self) -> f64 {
        self.z
    }
    pub fn new(x: f64, y: f64, z: f64) -> Self {
        Self { x, y, z }
    }

    pub fn ones() -> Self {
        Self::new(1.0, 1.0, 1.0)
    }

    pub fn zero() -> Self {
        Self::new(0.0, 0.0, 0.0)
    }

    pub fn near_zero(&self) -> bool {
        self.x() < 0.00000001
            && self.x() > -0.00000001
            && self.y() < 0.00000001
            && self.y() > -0.00000001
            && self.z() < 0.00000001
            && self.z() > -0.00000001
    }

    pub fn squared_length(&self) -> f64 {
        self.x * self.x + self.y * self.y + self.z * self.z
    }

    pub fn length(&self) -> f64 {
        self.squared_length().sqrt()
    }

    pub fn unit_vector(v: Self) -> Self {
        v / v.length()
    }

    pub fn dot(u: Vec3, v: Vec3) -> f64 {
        u.x * v.x + u.y * v.y + u.z * v.z
    }

    //叉乘
    //a = Vec3::new(1.0,2.0,3.0)，b = Vec3::new(1.0,2.0,3.0), (a.cross(b) == Vec3::zero()) = True
    pub fn cross(&self, other: Vec3) -> Self {
        let x = self.y * other.z - self.z * other.y;
        let y = self.z * other.x - self.x * other.z;
        let z = self.x * other.y - self.y * other.x;
        Vec3 {
            x: (x),
            y: (y),
            z: (z),
        }
    }

    pub fn random() -> Self {
        Self { x:random_double(), y:random_double(), z:random_double() }
    }
        
    pub fn random_range(min: f64, max: f64) -> Self {
        Self { x:random_double_range(min, max), y:random_double_range(min, max), z:random_double_range(min, max)}
    }

    pub fn random_in_unit_sphere() -> Vec3 {
        loop{//无限循环
            let p = Vec3::random_range(-1.0, 1.0);
            if p.squared_length() < 1.0 {
                return p;
            }
        }
    }

    pub fn random_unit_vector() -> Vec3 {
        return Vec3::unit_vector(Vec3::random_in_unit_sphere());
    }

    pub fn random_on_hemisphere(normal: Vec3) -> Vec3 {
        let on_unit_sphere = Self::random_in_unit_sphere();
        if Self::dot(on_unit_sphere, normal) > 0.0 {
           on_unit_sphere
        } else {
           -on_unit_sphere
        }
    }
    
    pub fn reflect(v: Vec3, n: Vec3) -> Vec3 {//反射函数
        v - 2.0 * Self::dot(v, n) * n
    }

    pub fn refract(uv: Vec3, n: Vec3, etai_over_etat: f64) -> Vec3 {
        let cos_theta = Self::dot(-uv, n).min(1.0);
        let r_out_perp = etai_over_etat * (uv + cos_theta * n);
        let r_out_parallel = -(1.0 - r_out_perp.squared_length()).abs().sqrt() * n;
        r_out_perp + r_out_parallel
    }
    
    pub fn info(&self) {
        println!("x={},y={},z={}", self.x, self.y, self.z);
    }
}

